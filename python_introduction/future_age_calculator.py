# Building a simple future age calculator 

#pseudocode 
# assume a current year of 2023
# promt user to input his or her current age
# add 27 years to get the  age in 2050 assuming we are i 2023
# print the result
current_age = float(input("How old are you? : ")) #prompting users to import current age
age_2050 = current_age + 27 # adding 27 year to the input from user , assuming that we are in 2023, to get the age in 2050
print("In 2050, you will be ", age_2050, "years old.") # display output of future age 2050
